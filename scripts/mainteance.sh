#!/bin/bash

echo "<=============================================== Now Starting Maintenance! ===============================================>"
echo "<=============================================== Begin System Update =====================================================>"
sudo pacman -Syu
echo "<=============================================== Remove Un-needed Packges ================================================>"
sudo pacman -Rsn $(pacman -Qtdq)
echo "<=============================================== Clean Pacman Cache ======================================================>"
sudo paccache -rk1
echo "<=============================================== Clean System Logs =======================================================>"
sudo journalctl --vacuum-time=7d
echo "<=============================================== Find broken symlinks ====================================================>"
sudo find / -xtype l -print
